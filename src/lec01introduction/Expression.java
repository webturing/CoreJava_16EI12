package lec01introduction;

public class Expression {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(1.0 * a / b);
        System.out.println(a % b);
    }
}
